// Helper to format downloads (e.g., 9000000 -> 9.0M)
function formatDownloads(n) {
  if (n >= 1000000) return (n / 1000000).toFixed(1) + "M";
  if (n >= 1000) return (n / 1000).toFixed(0) + "K";
  return n;
}

// Helper to get ratings scaled visually for the chart
function getScaledRatings(app) {
  // Hardcoded scale for App ID 2 to match the specific visual in the demo image (SmPlan)
  if (app.id === 2) {
    return [
      { name: "5 star", count: 28000, percentage: 100 },
      { name: "4 star", count: 14200, percentage: 85 },
      { name: "3 star", count: 6800, percentage: 55 },
      { name: "2 star", count: 3200, percentage: 35 },
      { name: "1 star", count: 1800, percentage: 20 },
    ];
  }
  // General fallback based on maximum count for other apps
  const maxCount = Math.max(...app.ratings.map((r) => r.count));
  return app.ratings.map((r) => ({
    ...r,
    percentage: maxCount > 0 ? (r.count / maxCount) * 100 : 0,
  }));
}
